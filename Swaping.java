package guviprg;

public class Swaping {

	public static void main(String[] args) {
		int x = 22, y = 33;
	System.out.println("Numbers before Swaping");
	System.out.println("x=" +x);
	System.out.println("y=" +y);
	int temp = x;
	x=y;
	y = temp;
	System.out.println("Numbers aftre  Swaping");
	System.out.println("x=" +x);
	System.out.println("y=" +y);

	}

}
